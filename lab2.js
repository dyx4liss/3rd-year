// Индивидуальное задание, 7й Вариант
// 1. Сортировка массива по возрастанию
function sortNumbers(arr) {
    arr.sort((a, b) => a - b);
}

// 2. Подсчет количества положительных чисел в массиве
function countPositiveNumbers(arr) {
    return arr.filter(num => num > 0).length;
}

// 3. Сортировка массива строк по алфавиту
function sortStrings(arr) {
    arr.sort();
}

// Пример использования
const numbers = [3, -1, 0, 5, -2, 8, -3];
const strings = ["banana", "apple", "orange", "grape"];

// Сортировка чисел
sortNumbers(numbers);
console.log("Отсортированный массив чисел:", numbers);

// Подсчет положительных чисел
const positiveCount = countPositiveNumbers(numbers);
console.log("Количество положительных чисел:", positiveCount);

// Сортировка строк
sortStrings(strings);
console.log("Отсортированный массив строк:", strings);
