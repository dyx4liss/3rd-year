// Создание массива с числами
const arr = [5, 15, 35, 25, 10];

// Инициализация переменной для хранения максимального значения
let max = arr[0];  // Предполагаем, что первый элемент — максимальный

// Использование цикла for для поиска наибольшего элемента
for (let i = 1; i < arr.length; i++) {
  if (arr[i] > max) {  // Сравниваем текущий элемент с max
    max = arr[i];  // Если текущий элемент больше, обновляем max
  }
}

// Вывод максимального значения
console.log("Максимальное значение в массиве: " + max); 

